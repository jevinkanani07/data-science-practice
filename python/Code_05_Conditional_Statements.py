# Topic: Conditional Statements (if,elif,else)

age = 19

# if conditional
if age >= 18:
  print("You are eligible to vote")

# if-else conditional
marks = 75

if marks >= 40:
  print("You are passsed ")
else:
  print("You are failed ")

# if-elif-else conditional
score = 82

if score >= 90:
  print("Grade: A")
elif score >= 75:
  print("Grade: B")
elif score >= 60:
  print("Grade: C")
else:
  print("Grade: D")
