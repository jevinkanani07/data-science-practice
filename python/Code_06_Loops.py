# for loop
print("For loop example :")
for i in range(1,6):
    print("Number:",i)

# while loop
print("While loop example:")
count = 1
while count <= 5:
    print("Count",count)
    count += 1

# break example
print("Break example:")
for i in range(1,10):
    if i ==5:
        break
    print(i)

# countinue example
print("Countinue example")
for i in range(1 , 10):
    if i == 5:
        continue
    print(i)

